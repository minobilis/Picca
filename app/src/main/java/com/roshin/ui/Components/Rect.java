package com.roshin.ui.Components;

public class Rect {

    public float x;
    public float y;
    public float width;
    public float height;

    public Rect() {

    }

    public Rect(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
